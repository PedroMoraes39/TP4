package modelo;

public class classificação {

	    public String verduras;
	    public String carnes;
	    public String limpeza;
	    public String frutas;
	    public String domesticos;

	public classificação() {
	    super();
	}


	public String getVerduras() {
	    return verduras;
	}


	public void setVerduras(String verduras) {
	    this.verduras = verduras;
	}


	public String getCarnes() {
	    return carnes;
	}


	public void setCarnes(String carnes) {
	    this.carnes = carnes;
	}


	public String getLimpeza() {
	    return limpeza;
	}


	public void setLimpeza(String limpeza) {
	    this.limpeza = limpeza;
	}


	public String getFrutas() {
	    return frutas;
	}


	public void setFrutas(String frutas) {
	    this.frutas = frutas;
	}


	public String getDomesticos() {
	    return domesticos;
	}


	public void setDomesticos(String domesticos) {
	    this.domesticos = domesticos;
	}




}
